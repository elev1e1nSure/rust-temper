    private static readonly Dictionary<string, CommandPreset> ByCommand = BuildLookup();

    private static Dictionary<string, CommandPreset> BuildLookup()
    {
        var map = new Dictionary<string, CommandPreset>();
        foreach (var preset in Presets)
        {
            map[preset.Command] = preset;
        }

        return map;
    }

    public static string DescriptionFor(string command) =>
        ByCommand.TryGetValue(command, out var preset)
            ? preset.Description
            : "Команда не описана в словаре — задайте бинд через список, чтобы увидеть подсказку.";

    public static CommandPreset? PresetFor(string command) =>
        ByCommand.TryGetValue(command, out var preset) ? preset : null;
